def _get_entityseg_instance_meta():
    # The following metadata maps contiguous id from [0, #thing categories +
    # #stuff categories) to their names and colors. We have to replica of the
    # same name and color under "thing_*" and "stuff_*" because the current
    # visualization function in D2 handles thing and class classes differently
    # due to some heuristic used in Panoptic FPN. We keep the same naming to
    # enable reusing existing visualization functions.

    thing_stuff_classes = [k["name"] for k in Entityseg_INST_CATEGORIES]
    thing_stuff_ids = [k["id"] for k in Entityseg_INST_CATEGORIES]
    thing_stuff_dataset_id_to_contiguous_id = {
        k: i for i, k in enumerate(thing_stuff_ids)
    }

    all_colors = {k["id"]: k["color"] for k in Entityseg_INST_CATEGORIES}
    thing_stuff_colors = [all_colors[k["id"]] for k in Entityseg_INST_CATEGORIES]

    thing_classes = [k["name"] for k in Entityseg_INST_CATEGORIES if k["isthing"] == 1]
    thing_ids = [k["id"] for k in Entityseg_INST_CATEGORIES if k["isthing"] == 1]
    thing_colors = [all_colors[k["id"]] for k in Entityseg_INST_CATEGORIES if k["isthing"] == 1]
    thing_dataset_id_to_contiguous_id = {k: i for i, k in enumerate(thing_ids)}

    meta = {
        "thing_dataset_id_to_contiguous_id": thing_stuff_dataset_id_to_contiguous_id,
        "thing_classes": thing_stuff_classes,
        "thing_colors": thing_stuff_colors,
        "thing_only_dataset_id_to_contiguous_id": thing_dataset_id_to_contiguous_id,
        "thing_only_classes": thing_classes,
        "thing_only_colors": thing_colors,
    }

    return meta

